"""
NEXAH Symmetry Graph Experiment
--------------------------------

Explores how geometric symmetries (17-29-5 structure)
generate regime landscapes inside the NEXAH kernel.

Structure:

center node
17 radial spokes
pentagon ring (5)
hexagon ring (6)
cycle structure

Goal:
Investigate attractor basins and regime transitions
generated by symmetry-driven graph topology.
"""

import networkx as nx
import random


# -------------------------
# Graph Construction
# -------------------------

def build_symmetry_graph():

    G = nx.Graph()

    center = "center"
    G.add_node(center)

    # 17 spokes
    spokes = [f"s{i}" for i in range(17)]

    for s in spokes:
        G.add_edge(center, s, weight=1.0)

    # pentagon ring
    pentagon = spokes[:5]

    for i in range(5):
        G.add_edge(pentagon[i], pentagon[(i+1) % 5], weight=2.0)

    # hexagon ring
    hexagon = spokes[5:11]

    for i in range(6):
        G.add_edge(hexagon[i], hexagon[(i+1) % 6], weight=1.5)

    # cycle structure (29 style)
    for i in range(len(spokes)-1):
        G.add_edge(spokes[i], spokes[i+1], weight=0.5)

    return G


# -------------------------
# Simple Regime Detector
# -------------------------

def detect_regime(G):

    degree_var = nx.degree_histogram(G)

    if len(degree_var) > 5:
        return "STRESS"

    if len(degree_var) < 3:
        return "STABLE"

    return "TRANSITION"


# -------------------------
# Random Drift Simulation
# -------------------------

def simulate_drift(G, steps=20):

    history = []

    for t in range(steps):

        # random structural perturbation
        u, v = random.choice(list(G.edges()))

        if random.random() < 0.3:
            G.remove_edge(u, v)

        regime = detect_regime(G)

        history.append(regime)

    return history


# -------------------------
# Run Experiment
# -------------------------

if __name__ == "__main__":

    G = build_symmetry_graph()

    print("Nodes:", G.number_of_nodes())
    print("Edges:", G.number_of_edges())

    regimes = simulate_drift(G)

    print("Regime trajectory:")
    print(regimes)
